public class HelloWorld {
    // Ini adalah deklarasi dari kelas yang bernama 'HelloWorld'.
    // 'public' berarti kelas ini dapat diakses dari kelas lain.

    public static void main(String[] args) {
        /*
        - Ini adalah metode 'main', yang merupakan titik masuk dari setiap aplikasi Java.
        - 'public' berarti metode ini dapat diakses dari mana saja.
        - 'static' berarti metode ini milik kelas 'HelloWorld' dan
           bukan milik sebuah instansi tertentu dari kelas tersebut.
        - 'void' berarti metode ini tidak mengembalikan nilai apapun.
        - 'main' adalah nama dari metode tersebut.
        - 'String[] args' adalah sebuah array dari objek 'String' yang bernama 'args',
           yang dapat menerima argumen baris perintah.
         */
        System.out.println("Hello World");
    }
}
