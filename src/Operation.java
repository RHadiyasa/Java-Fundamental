public class Operation {
    public static void main(String[] args) {
        // Operator Aritmatika
        int a = 10;
        int b = 5;

        System.out.println(a + b); // Penjumlahan
        System.out.println(a - b); // Pengurangan
        System.out.println(a * b); // Perkalian
        System.out.println(a / b); // Pembagian
    }
}
