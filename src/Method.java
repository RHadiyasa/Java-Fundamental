public class Method {
    // Ini adalah deklarasi kelas bernama Method
    // Keyword public berarti kelas dapat diakses secara public oleh kelas lain
    public static void main(String[] args) { // Ini adalah main method -> entry point

        helloWorld(); // -> Memanggil method helloWorld yang sudah dibuat.

    }

    static void helloWorld(){ // Membuat method bernama helloWorld
        System.out.println("Hello World!");
    }
}
